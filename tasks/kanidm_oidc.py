"""Kanidm OIDC integration: create person accounts and configure OAuth2 clients.

Assumes kanidm is running and healthy. Safe to re-run — all steps are idempotent.
Driven by KANIDM_PERSONS and KANIDM_OIDC_CLIENTS in group_data/all.py.

Uses the Kanidm REST API directly (the server image is distroless and has no
CLI binary; the CLI requires a TTY for password input and cannot be automated).
Authenticates as idm_admin (not admin — only idm_admin can manage persons and
OAuth2 clients).

OIDC client secrets are generated by Kanidm (cannot be set externally). On first
deploy the secret is read from the API and saved to Bitwarden. Subsequent deploys
read it from BW via secrets.py. A second deploy (or targeted
`tasks/secrets.py tasks/vaultwarden.py`) is needed after first setup to
propagate the generated secret to downstream services.
"""

