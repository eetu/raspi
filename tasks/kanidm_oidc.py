# --- Save credentials to Bitwarden (runs locally) ---


def _ssh_cat(path):
    """Read a file from the Pi via SSH, return contents or empty string."""
    r = subprocess.run(
        ["ssh", "raspi", f"sudo cat {path} 2>/dev/null"],
        capture_output=True,
        text=True,
    )
    return r.stdout.strip()


def _save_to_bitwarden(state=None, host=None):
    # Batch all changes to the kanidm BW item into a single edit to avoid
    # revision conflicts from multiple rapid edits.
    item = json.loads(json.dumps(bw._get_item("kanidm")))  # copy
    changed = False

    fields = item.get("fields") or []

    # Admin + idm_admin passwords (both recovered on first boot)
    for account, field_name in [
        ("admin", "admin_password"),
        ("idm_admin", "idm_admin_password"),
    ]:
        password = _ssh_cat(f"/etc/secrets/kanidm-{account}-password")
        if not password:
            continue
        existing = next((f for f in fields if f["name"] == field_name), None)
        if existing:
            if existing["value"] != password:
                existing["value"] = password
                changed = True
                logger.info(f"kanidm: {field_name} updated in Bitwarden")
        else:
            fields.append({"name": field_name, "value": password, "type": 1})
            changed = True
            logger.info(f"kanidm: {field_name} saved to Bitwarden")

    # OIDC client secrets (generated by Kanidm, read from API)
    for client_name, cfg in KANIDM_OIDC_CLIENTS.items():
        secret = _ssh_cat(f"/var/lib/kanidm/.oidc-secret-{client_name}").strip('"')
        if not secret:
            continue
        field_name = cfg["secret_field"]
        existing = next((f for f in fields if f["name"] == field_name), None)
        if existing:
            if existing["value"] != secret:
                existing["value"] = secret
                changed = True
                logger.info(f"kanidm: {client_name} OIDC secret updated in Bitwarden")
        else:
            fields.append({"name": field_name, "value": secret, "type": 1})
            changed = True
            logger.info(f"kanidm: {client_name} OIDC secret saved to Bitwarden")

    # Per-person reset tokens
    for username in KANIDM_PERSONS:
        token = _ssh_cat(f"/var/lib/kanidm/.reset-token-{username}")
        if not token:
            continue

        logger.info(f"kanidm: credential reset token for {username}:\n{token}")

        field_name = f"{username}_reset_token"
        existing = next((f for f in fields if f["name"] == field_name), None)
        if existing:
            if not existing["value"]:
                existing["value"] = token
                changed = True
                logger.info(f"kanidm: saved {username} reset token to Bitwarden")
        else:
            fields.append({"name": field_name, "value": token, "type": 1})
            changed = True
            logger.info(f"kanidm: saved {username} reset token to Bitwarden")

        # Clean up temp file
        subprocess.run(
            ["ssh", "raspi", f"sudo rm -f /var/lib/kanidm/.reset-token-{username}"],
            capture_output=True,
        )

    if changed:
        item["fields"] = fields
        bw._edit_item(item)
        logger.info("kanidm: Bitwarden item updated")


python.call(
    name="Save Kanidm credentials to Bitwarden",
    function=_save_to_bitwarden,
)
