import subprocess

from pyinfra import logger
from pyinfra.operations import python, server

import vault as bw
from group_data.all import KANIDM, KANIDM_OIDC_CLIENTS, KANIDM_PERSONS, NETWORK

DOMAIN = NETWORK["domain"]

